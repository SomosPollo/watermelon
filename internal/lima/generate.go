type provisionBuild struct {
	BaseImage  string
	CustomTag  string
	InstallCmd string
	Packages   []string
	BinDirs    string
	ExposeBins []string
}

type smartWrapper struct {
	Cmd         string // "npm", "pip", etc.
	BaseImage   string // original container image
	CustomTag   string // "watermelon-npm", etc.
	BinDirs     string // dirs to scan for new binaries
	GlobalCheck string // shell snippet setting _wm_global=true
}

type templateData struct {
	CPUs            int
	Memory          string
	Disk            string
	ProjectDir      string
	ToolsDir        string
	NetworkAllow    []string
	NetworkProcess  map[string][]string
	PortForwards    []int
	Tools           map[string][]string
	ProvisionBuilds []provisionBuild
	SmartWrappers   []smartWrapper
}

// GenerateConfig creates Lima YAML from watermelon config
func GenerateConfig(cfg *config.Config, projectDir string) (string, error) {
	// Validate network allow domains
	for _, domain := range cfg.Network.Allow {
		if err := validateDomain(domain); err != nil {
			return "", fmt.Errorf("invalid network allow domain: %w", err)
		}
	}

	// Validate network process names and domains
	for processName, domains := range cfg.Network.Process {
		if processName == "" {
			return "", fmt.Errorf("process name cannot be empty")
		}
		if strings.ContainsAny(processName, shellMetacharacters+" /") {
			return "", fmt.Errorf("invalid process name %q: contains invalid characters", processName)
		}
		for _, domain := range domains {
			if err := validateDomain(domain); err != nil {
				return "", fmt.Errorf("invalid domain for process %q: %w", processName, err)
			}
		}
	}

	// Validate port forwards
	for _, port := range cfg.Ports.Forward {
		if err := validatePort(port); err != nil {
			return "", fmt.Errorf("invalid port forward: %w", err)
		}
	}

	// Build provision data: custom images and tool image overrides
	type provSpec struct {
		pkgs       []string
		cmd        string
		installCmd string
		tag        string
		binDirs    string
	}
	specs := []provSpec{
		{cfg.Provision.Npm, "npm", "npm install -g", "watermelon-npm", "/usr/local/bin"},
		{cfg.Provision.Pip, "pip", "pip install", "watermelon-pip", "/usr/local/bin"},
		{cfg.Provision.Cargo, "cargo", "cargo install", "watermelon-cargo", "/usr/local/cargo/bin /usr/local/bin"},
		{cfg.Provision.Go, "go", "go install", "watermelon-go", "/go/bin /usr/local/bin"},
		{cfg.Provision.Gem, "gem", "gem install", "watermelon-gem", "/usr/local/bin"},
	}

	var provisionBuilds []provisionBuild
	imageOverrides := make(map[string]string)

	for _, spec := range specs {
		if len(spec.pkgs) == 0 {
			continue
		}
		for _, pkg := range spec.pkgs {
			if err := validatePackageName(pkg); err != nil {
				return "", fmt.Errorf("invalid %s package: %w", spec.cmd, err)
			}
		}
		img := findImageForCommand(cfg.Tools, spec.cmd)
		if img == "" {
			return "", fmt.Errorf("provision.%s requires %s in [tools]; add a container image that provides %s", spec.cmd, spec.cmd, spec.cmd)
		}
		imageOverrides[img] = spec.tag
		provisionBuilds = append(provisionBuilds, provisionBuild{
			BaseImage:  img,
			CustomTag:  spec.tag,
			InstallCmd: spec.installCmd,
			Packages:   spec.pkgs,
			BinDirs:    spec.binDirs,
			ExposeBins: provisionExposeBins(spec.cmd, spec.pkgs),
		})
	}

	// Apply image overrides so tool wrappers use custom images with packages pre-installed
	tools := make(map[string][]string)
	for image, cmds := range cfg.Tools {
		if override, ok := imageOverrides[image]; ok {
			tools[override] = cmds
		} else {
			tools[image] = cmds
		}
	}

	funcMap := template.FuncMap{
		"add": func(a, b int) int { return a + b },
		"isWildcard": func(s string) bool {
			return strings.HasPrefix(s, "*.")
		},
		"baseDomain": func(s string) string {
			return strings.TrimPrefix(s, "*.")
		},
		"indent": func(n int, s string) string {
			pad := strings.Repeat(" ", n)
			return pad + strings.ReplaceAll(s, "\n", "\n"+pad)
		},
		"join": strings.Join,
	}
	tmpl, err := template.New("lima").Funcs(funcMap).Parse(limaTemplate)
	if err != nil {
		return "", fmt.Errorf("parsing template: %w", err)
	}

	// Generate smart wrappers for package manager commands found in tools
	type wrapperSpec struct {
		cmd         string
		customTag   string
		binDirs     string
		globalCheck string
	}
	wrapperSpecs := []wrapperSpec{
		{"npm", "watermelon-npm", "/usr/local/bin",
			`for _arg in "$@"; do` + "\n" + `  case "$_arg" in -g|--global) _wm_global=true ;; esac` + "\n" + `done`},
		{"pip", "watermelon-pip", "/usr/local/bin",
			`case "$1" in install) _wm_global=true ;; esac`},
		{"cargo", "watermelon-cargo", "/usr/local/cargo/bin /usr/local/bin",
			`case "$1" in install) _wm_global=true ;; esac`},
		{"go", "watermelon-go", "/go/bin /usr/local/bin",
			`case "$1" in install) _wm_global=true ;; esac`},
		{"gem", "watermelon-gem", "/usr/local/bin",
			`case "$1" in install) _wm_global=true ;; esac`},
	}

	var smartWrappers []smartWrapper
	for _, ws := range wrapperSpecs {
		if img := findImageForCommand(cfg.Tools, ws.cmd); img != "" {
			smartWrappers = append(smartWrappers, smartWrapper{
				Cmd:         ws.cmd,
				BaseImage:   img,
				CustomTag:   ws.customTag,
				BinDirs:     ws.binDirs,
				GlobalCheck: ws.globalCheck,
			})
		}
	}

	data := templateData{
		CPUs:            cfg.Resources.CPUs,
		Memory:          convertMemory(cfg.Resources.Memory),
		Disk:            convertDisk(cfg.Resources.Disk),
		ProjectDir:      projectDir,
		ToolsDir:        "",
		NetworkAllow:    cfg.Network.Allow,
		NetworkProcess:  cfg.Network.Process,
		PortForwards:    cfg.Ports.Forward,
		Tools:           tools,
		ProvisionBuilds: provisionBuilds,
		SmartWrappers:   smartWrappers,
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, data); err != nil {
		return "", fmt.Errorf("executing template: %w", err)
	}

	return buf.String(), nil
}

// provisionExposeBins returns best-effort CLI command names that should get wrappers
// for provisioned packages even when they already exist in the base image.
func provisionExposeBins(manager string, pkgs []string) []string {
	if manager != "npm" {
		return nil
	}

	seen := make(map[string]bool)
	var bins []string
	for _, pkg := range pkgs {
		bin := npmPackageToCommand(pkg)
		if bin == "" || seen[bin] {
			continue
		}
		seen[bin] = true
		bins = append(bins, bin)
	}
	return bins
}

// npmPackageToCommand converts npm package specs to likely command names:
// - "pnpm" -> "pnpm"
// - "pnpm@10" -> "pnpm"
// - "@scope/name" -> "name"
// - "@scope/name@1.2.3" -> "name"
func npmPackageToCommand(pkg string) string {
	if pkg == "" {
		return ""
	}

	name := pkg
	if strings.HasPrefix(name, "@") {
		if slash := strings.LastIndex(name, "/"); slash >= 0 && slash+1 < len(name) {
			name = name[slash+1:]
		}
	}

	if at := strings.LastIndex(name, "@"); at > 0 {
		name = name[:at]
	}

	name = strings.TrimSpace(name)
	if name == "" {
		return ""
	}
	if strings.ContainsAny(name, shellMetacharacters+" /") {
		return ""
	}
	return name
}

// convertMemory converts "4GB" to "4GiB" for Lima
func convertMemory(mem string) string {
	return strings.Replace(mem, "GB", "GiB", 1)
}

// convertDisk converts "20GB" to "20GiB" for Lima
func convertDisk(disk string) string {
	return strings.Replace(disk, "GB", "GiB", 1)
}
